    csv_reader = csv.reader(input_file)
    csv_writer = csv.writer(output_file)
    

    for row in csv_reader:

        for i in range(len(row)):
            cell = row[i]
            
            if cell in replacement_table:
                row[i] = replacement_table[cell]
        
        csv_writer.writerow(row)